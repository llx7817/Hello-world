package cn.art.model;

public class JWord {
    private Integer jwid;

    private Integer tid;

    private Integer wid;

    private Integer couplewid;

    public Integer getJwid() {
        return jwid;
    }

    public void setJwid(Integer jwid) {
        this.jwid = jwid;
    }

    public Integer getTid() {
        return tid;
    }

    public void setTid(Integer tid) {
        this.tid = tid;
    }

    public Integer getWid() {
        return wid;
    }

    public void setWid(Integer wid) {
        this.wid = wid;
    }

    public Integer getCouplewid() {
        return couplewid;
    }

    public void setCouplewid(Integer couplewid) {
        this.couplewid = couplewid;
    }
}